INSERT INTO	#volumen_ocs(orc_orden, orc_proveedor, orc_ferecep,wk_ferecep, qty, oc_type)
select		O.ORC_ORDEN, O.ORC_PROVEEDOR, O.ORC_FERECEP, DATEPART(wk,convert(varchar,O.ORC_FERECEP)), SUM(
			case when aBP.ArticuloId is NOT null then ARO_EMPORD * aBP.FactorBreakPack/100 else A.ARO_EMPORD end), 
			case when P.XXX_ORIGEN like '%T%' then 'PB' else 'POR' end as TIPO
from		BDCMP..OORCO O
INNER JOIN	BDCMP..OAROR A ON O.ORC_ORDEN = A.ARO_ORDEN
INNER JOIN	BDCMP..OPROV P on o.ORC_PROVEEDOR = P.PRO_PROVEEDOR
LEFT JOIN	INTERFASE..exe_imp_sdircvdti r on o.ORC_ORDEN = r.PMG_PO_NUMBER
left join	(select distinct articuloiD, FactorBreakPack from ArticuloBreakPack) aBP on a.aro_articulo = aBP.articuloId
WHERE		orc_sucursal in (2160) and --OCs de la bodega
			ORC_ESTADO in (4) and-- OCs ordenadas
			ORC_FERECEP >= CONVERT(varchar,getdate()-@max_days_back,112) AND
			ORC_CONVENIO in (1)--OCs POR
			and ORC_PROVEEDOR not in (1)
			and r.PMG_PO_NUMBER is null--No recibidas
			AND O.ORC_PROVEEDOR IN (SELECT V.prov_id FROM #whse_schedule V)
group by	ORC_ORDEN, ORC_PROVEEDOR, ORC_FERECEP, P.XXX_ORIGEN

