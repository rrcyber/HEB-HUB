declare @current_date int;
declare @correct_date int;
declare @pmg_po_number bigint;
set @prov_id = 0;
set @current_date = 0;
set @correct_date = 0;

select @prov_id = MIN(orc_proveedor) from #volumen_ocs where final_date is null;
while(@prov_id is not null)begin
	select @pmg_po_number = MIN(orc_orden)
	from #volumen_ocs where final_date is null and orc_proveedor = @prov_id;
	while(@pmg_po_number is not null)begin
		
		select @current_date = orc_ferecep from #volumen_ocs where orc_orden = @pmg_po_number;
		
		select @correct_date = max(d.date_equivalent) from #whse_schedule w inner join #dates d on w.day_number = d.day_number where w.prov_id in (@prov_id)
		and d.date_equivalent < @current_date
		
		if(@correct_date is null)begin
			select @correct_date = min(d.date_equivalent) from #whse_schedule w inner join #dates d on w.day_number = d.day_number where w.prov_id in (@prov_id)
			and d.date_equivalent > @current_date
		end
		
		update #volumen_ocs set final_date = @correct_date
		where orc_proveedor = @prov_id and orc_orden = @pmg_po_number;
		
		select @pmg_po_number = MIN(orc_orden)
		from #volumen_ocs where final_date is null and orc_proveedor = @prov_id and orc_orden > @pmg_po_number;
	end
	select @prov_id = MIN(orc_proveedor) from #volumen_ocs where final_date is null and orc_proveedor > @prov_id;
end

